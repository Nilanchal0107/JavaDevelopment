class DataType {
    public static void main (String A[]) {
        byte a = 127;
        short b = 32767;
        int c = 1_00_00_000;
        long d = 123456l;
        float e = 5.0f;
        char f = 'a';
        f++;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);


    }
}